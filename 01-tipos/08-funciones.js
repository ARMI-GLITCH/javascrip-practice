// Una función es un bloque de código que puede realizar una tarea específica.
// En este caso, definimos una funcion llamada saludar
function saludar() {
  console.log("Hola mundo");
}
// Llamamos o ejecutamos la funcion 
saludar(); // Se puede reutilizar llamándola por su nombre.

// Definimos otra función llamada suma
function suma() {
  return 2 + 2; // return sirve para devolver el resultado de la suma 2 + 2
}

// Llamamos a la función suma y guardamos el resultado en una variable para luego imprimirla
let resultado = suma();
console.log(resultado);
